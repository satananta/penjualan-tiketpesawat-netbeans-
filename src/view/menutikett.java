    public void judul() {
        Object[] judul = {
            "ID Tiket", "Harga", "Kelas", "Status"
        };
        tabModel = new DefaultTableModel (null, judul);
        tabeltiket.setModel(tabModel);
    }
    public void tampilData(String where) {
        try {
            st = con.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs=st.executeQuery("SELECT * FROM tiket" + where);
            
            while (rs.next()){
                Object[] data = {
                    rs.getString("id_tiket"),
                    rs.getString("harga"),
                    rs.getString("kelas"),
                    rs.getString("status"),
                };
                tabModel.addRow(data);
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    
