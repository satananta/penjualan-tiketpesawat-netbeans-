    public void setIdTiket(String p){
        o.setid_tiket(p);
        txtNomorTiket.setText(p);
        txtHarga.setText(o.getid_tiket().get(1));
        txtKelas.setText(o.getid_tiket().get(2));
        txtStatus.setText(o.getid_tiket().get(3));
    }
    
    private void setDataFromTable(int row) {
    // Mendapatkan data dari tabel untuk baris yang diklik
    String nomortiket = tabeltiket.getValueAt(row, 0).toString();
    String harga = tabeltiket.getValueAt(row, 1).toString();
    String kelas = tabeltiket.getValueAt(row, 2).toString();
    String status = tabeltiket.getValueAt(row, 3).toString();
    
    // Mengisi data pada panelAdd
    txtNomorTiket.setText(nomortiket);
    txtHarga.setText(harga);
    txtKelas.setText(kelas);
    txtStatus.setText(status);
    }
    
